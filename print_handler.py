    def print_task(self, id, task):
        print(f"\033[94m[\033[1mTask \033[92m#{id}\033[0m\033[94m]\033[0m {task}")

    def print_super_task(self, super_task, total_tasks):
        print(f"\033[93m[\033[1mSuper Task\033[0m\033[93m]\033[0m {super_task}")

    def print_loading(self):
        self.stop_loading.clear()
        th.Thread(target=self.__print_loading_loop).start()

    def __print_loading_loop(self):
        print("\033[94m[\033[1mThinking\033[0m\033[94m]\033[0m", end=" ")
        while not self.stop_loading.is_set():
            sys.stdout.write("\r\033[K")
            print("\033[94m[\033[1mThinking\033[0m\033[94m]\033[0m", end=" ")
            sys.stdout.flush()
            for i in range(0, 3):
                if self.stop_loading.is_set():
                    break
                sys.stdout.write("\033[94m.\033[0m")
                sys.stdout.flush()
                time.sleep(0.5)
    
    def break_loading(self):
        self.stop_loading.set()
        print("\033[94m[\033[1mDone\033[0m\033[94m]\033[0m")
        time.sleep(0.5)